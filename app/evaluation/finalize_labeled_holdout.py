# app/evaluation/finalize_labeled_holdout.py

from __future__ import annotations

import argparse
import csv
import json
from pathlib import Path
from typing import Any

ALLOWED_STATUSES = {"recommended", "human_review_required", "evidence_insufficient", "excluded"}
TRUE_VALUES = {"true", "1", "yes", "y", "t"}
FALSE_VALUES = {"false", "0", "no", "n", "f"}
CSV_ENCODINGS = ("utf-8-sig", "utf-8", "cp949", "euc-kr")


def normalize_text(value: Any) -> str:
    return str(value or "").replace("\ufeff", "").replace("\u00a0", " ").strip()


def open_text_with_fallback(path: str | Path) -> tuple[str, str]:
    input_path = Path(path)
    last_error: UnicodeDecodeError | None = None
    for encoding in CSV_ENCODINGS:
        try:
            return input_path.read_text(encoding=encoding), encoding
        except UnicodeDecodeError as error:
            last_error = error
    if last_error:
        raise last_error
    return input_path.read_text(encoding="utf-8"), "utf-8"


def load_jsonl(path: str | Path) -> list[dict[str, Any]]:
    rows: list[dict[str, Any]] = []
    with Path(path).open("r", encoding="utf-8") as file:
        for line in file:
            line = line.strip()
            if line:
                rows.append(json.loads(line))
    return rows


def load_labels_from_csv(path: str | Path) -> dict[str, dict[str, Any]]:
    labels: dict[str, dict[str, Any]] = {}
    text, encoding = open_text_with_fallback(path)
    reader = csv.DictReader(text.splitlines())
    normalized_fieldnames = [normalize_text(name) for name in (reader.fieldnames or [])]
    if "case_id" not in normalized_fieldnames:
        raise ValueError(f"CSV must include case_id column. Detected encoding={encoding}, columns={normalized_fieldnames}")

    for raw_row in reader:
        row = {normalize_text(key): value for key, value in raw_row.items()}
        case_id = normalize_text(row.get("case_id"))
        if not case_id:
            continue
        labels[case_id] = {
            "expected_status": normalize_text(row.get("expected_status")),
            "expected_requires_human_review": parse_bool(row.get("expected_requires_human_review")),
        }
    return labels


def parse_bool(value: Any) -> bool | None:
    if isinstance(value, bool):
        return value
    normalized = normalize_text(value).lower()
    if normalized in TRUE_VALUES:
        return True
    if normalized in FALSE_VALUES:
        return False
    return None


def validate_label(case_id: str, expected_status: str, expected_review: bool | None) -> None:
    if expected_status not in ALLOWED_STATUSES:
        raise ValueError(f"{case_id}: invalid expected_status '{expected_status}'. Allowed: {sorted(ALLOWED_STATUSES)}")
    if expected_review is None:
        raise ValueError(f"{case_id}: expected_requires_human_review must be true/false")


def merge_labels(unlabeled_rows: list[dict[str, Any]], labels: dict[str, dict[str, Any]]) -> list[dict[str, Any]]:
    merged: list[dict[str, Any]] = []
    missing: list[str] = []
    for row in unlabeled_rows:
        case_id = normalize_text(row.get("case_id"))
        label = labels.get(case_id)
        if not label:
            missing.append(case_id)
            continue
        expected_status = normalize_text(label["expected_status"])
        expected_review = label["expected_requires_human_review"]
        validate_label(case_id, expected_status, expected_review)
        copied = dict(row)
        copied["expected_status"] = expected_status
        copied["expected_requires_human_review"] = expected_review
        copied.pop("labeling_note", None)
        merged.append(copied)

    if missing:
        raise ValueError(f"Missing labels for case_id(s): {', '.join(missing[:20])}")
    return merged


def write_jsonl(path: str | Path, rows: list[dict[str, Any]], append: bool = False) -> None:
    output_path = Path(path)
    output_path.parent.mkdir(parents=True, exist_ok=True)
    mode = "a" if append else "w"
    with output_path.open(mode, encoding="utf-8") as file:
        for row in rows:
            file.write(json.dumps(row, ensure_ascii=False) + "\n")


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser()
    parser.add_argument("--unlabeled-jsonl", required=True, help="JSONL generated by export_holdout_candidates")
    parser.add_argument("--labeled-csv", required=True, help="CSV after manual expected_status labeling")
    parser.add_argument("--output-jsonl", default="outputs/labeled_holdout_gold.jsonl")
    parser.add_argument("--append-to", default=None, help="Optional JSONL file to append the finalized cases to")
    return parser.parse_args()


def main() -> None:
    args = parse_args()
    rows = merge_labels(
        unlabeled_rows=load_jsonl(args.unlabeled_jsonl),
        labels=load_labels_from_csv(args.labeled_csv),
    )
    write_jsonl(args.output_jsonl, rows, append=False)
    print(f"finalized_cases={len(rows)}")
    print(f"output_jsonl={args.output_jsonl}")
    if args.append_to:
        write_jsonl(args.append_to, rows, append=True)
        print(f"appended_to={args.append_to}")


if __name__ == "__main__":
    main()
